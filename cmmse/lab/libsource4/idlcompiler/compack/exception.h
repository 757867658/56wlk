#ifndef __COMPACK_EXCEPTION_H_
#define __COMPACK_EXCEPTION_H_

#define throw_mcpack_arg(error) \
    do{throw bsl::BadArgumentException() << BSL_EARG << error;}while(0)

#define throw_mcpack_data(error) \
    do{throw bsl::ParseErrorException() << BSL_EARG << error;}while(0)

#define throw_mcpack_type(error) \
    do{throw bsl::ParseErrorException() << BSL_EARG << error;}while(0)

#define throw_json_data(error) \
    do{throw bsl::ParseErrorException() << BSL_EARG << error;}while(0)

#define throw_ioexception(error) \
    do{throw bsl::OutOfBoundException() << BSL_EARG << error;}while(0)

#endif

